#' Square a number
#'
#' Take in any numeric value and square it
#' @param x A numeric value to be squared
#' @return The square of the input
#' @export
squared <- function(x){
  return(x^2)
}

#' Cube a number
#' @param x Number to be cubed
#' @return The cube of the input
#' @export
cube <- function(x) {
  return(x^3)
}

#' Quadrat a number
#' @param x Number to be quadrated
#' @return The quadrad of the input
#' @export
quadratic <- function(x){
  return(x^4)
}
